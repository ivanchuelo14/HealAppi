    public void Abrir(String cedula) {

        String nom = "", ape = "", Eda = "", Gener = "", Antecedentes = "", Pass = "";
        String sql = "SELECT * FROM General_User_Adm WHERE cedula='" + cedula + "'";
        Statement st;
        try {
            st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                nom = rs.getString("Nombre");
                ape = rs.getString("Apellido");
                Eda = rs.getNString("Edad");
                Pass = rs.getNString("Contrase√±a");
                Gener = rs.getNString("Genero");
                Antecedentes = rs.getNString("Antecedentes");
                Name.setText(nom);
                Apelli.setText(ape);
                ced.setText(cedula);
                Age.setText(Eda);
                Contra.setText(Pass);
            }
        } catch (SQLException ex) {
            Logger.getLogger(MainMenu.class.getName()).log(Level.SEVERE, null, ex);
        }
        ced.setEnabled(false);
        Modif.setVisible(true);
        jButton1.setVisible(false);
        jButton2.setVisible(false);

    }

